const fruits = ["Abacate", "Abacaxi", "Laranja", "Pêssego"];

console.log("Array original: ", fruits);

// Colocando um elemento no final do array
fruits.push("Uva");

console.log("Array após push: ", fruits);

// Removendo um elemento no final do array
fruits.pop();

console.log("Array após pop: ", fruits);
